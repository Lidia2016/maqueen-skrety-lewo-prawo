basic.forever(function () {
    maqueen.MotorRun(maqueen.aMotors.M1, maqueen.Dir.CW, 144)
    maqueen.MotorRun(maqueen.aMotors.M2, maqueen.Dir.CW, 144)
    basic.pause(1000)
    maqueen.MotorRun(maqueen.aMotors.M1, maqueen.Dir.CCW, 0)
    maqueen.MotorRun(maqueen.aMotors.M2, maqueen.Dir.CCW, 0)
    basic.pause(1000)
    maqueen.MotorRun(maqueen.aMotors.M1, maqueen.Dir.CCW, 144)
    maqueen.MotorRun(maqueen.aMotors.M2, maqueen.Dir.CCW, 144)
    basic.pause(1000)
})
